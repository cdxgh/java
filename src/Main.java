public class Main {
    public static void main(String[] args) {
        int[] arr = new int[10]; // Declare an array of size 10

        // Assign 5 to each element
        for (int i = 0; i < 10; i++) {
            arr[i] = 5;
        }

        // Print the array elements
        for (int i = 0; i < 10; i++) {
            System.out.println(arr[i]); // This will print '5' ten times
        }
    }
}
